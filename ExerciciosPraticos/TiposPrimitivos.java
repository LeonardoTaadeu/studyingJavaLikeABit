public class TiposPrimitivos {
    public static void main(String[] args) {
        //Os tipos primitivos sao os tipos que o Java aceita como variavel

        //Numeros inteiros
        byte numeroPequeno = 100; //numeros inteiros pequenos entre -128 e 128
        short numeroCurto = 30000;
        int idade = 24;
        long numeroGrandePraCaralho = 1999999999L; //O L aparece quando o numero ultrapassa a quantidade limite do inteiro 


        //Numeros decimais
        float altura = 1.92f;
        double salario = 2583.14;


        //Caracter
        char letra = 'A'; //Comporta somente um caracter


        //Booleano
        boolean ativo = true;
        boolean falso = false;


        //String
        String nome = "Leonardo";
        String mensagem = "Eae Leo, tudo bem?";
    }
}